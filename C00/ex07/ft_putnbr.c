#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		n;
	char	c;

	if (nb < 0)
	{
		write(1, "-", 1);
		if (nb == -2147483648)
		{
			write(1, "2", 1);
			write(1, "147483648", 9);
		}
		else
			ft_putnbr(nb * (-1));
	}
	else
	{
		n = nb / 10;
		if (n != 0)
			ft_putnbr(n);
		c = nb % 10 + 48;
		write(1, &c, 1);
	}
}
