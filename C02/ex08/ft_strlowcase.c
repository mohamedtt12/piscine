char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	if (!str[i])
		return (str);
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}
