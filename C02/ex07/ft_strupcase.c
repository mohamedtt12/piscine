char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	if (!str[i])
		return (str);
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= 32;
		i++;
	}
	return (str);
}
